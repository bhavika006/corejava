package com.corejava;

public class conditionifelse {
	public static void main(String[]args) {
		int time = 20;
		if(time<20) {
			System.out.println("good morning");
		}
		else if(time<10) {
			System.out.println("good afternoon");
		}
		else {
			System.out.println("good night");
		}
		
		
	}
	
}
